    <h1 class="text-5xl">Hvordan klatre i rangene og bli en bedre spiller</h1>
    <p>Overwatch 2 er et komplekst spill som krever kompetanse i mange forskjellige ting for å vinne kamper.</p>
    <img src="/src/lib/gamesense.jpeg" alt="" class="w-fit h-fit"> <!-- bilde fra google -->
    <p>Ferdighetene til en spiller deles ofte inn i to seksjoner: "gamesense" og "mechanics". "Gamesense" omfatter all kunnskapen og erfaringen du har som spiller og hvordan du bruker det i hver kamp. Enhver situasjon krever forskjellige tilnærminger og løsninger og desto bedre "gamesense" du har vil du være bedre forberedt på å gjøre de riktige valgene i kampen </p>
    <img src="/src/lib/mechanics.jpeg" alt="" class="w-fit h-fit"> <!-- bilde fra google -->

    <p>"mechanics" omfatter din tekniske evne som spiller. Dette inkluderer ting som reaksjonsevne, hvor flytende bevegelse dine er, hvor kjent du er med tastaturet, hvor god du er til å sikte, hvor kjapt du kan ta valg og utføre dem, etc</p>
    <p>Både gamesense og mechanics kan øves på, men gamesense er typisk noe du utvikler jo mer erfaring du får. Mechanics kan og bør øves på og du kan se store forbedringer etter litt trening på mechanics Det finnes en haug av youtube videoer som beskriver hvordan man kan forbedre sine mechanics og bli en bedre spiller. </p>
    <p>Jeg anbefaler å fokusere på å forbedre dine mechanics først og så tenke på gamesense senere ettersom gamesense kommer med erfaring og du kan klatre i rangene ganske mye med gode mechanics</p>
    <h1 class="text-5xl">Ranks</h1>
    <p>I overwatch 2 er "competetive mode" den mest spilte modusen. I competetive mode konkurerrer spillere om å oppnå høyest "rank". Vinner du kampen blir du belønnet med poeng og du mister poeng om du taper</p>
    <img src="/src/lib/ranks.png" alt="" class="w-fit h-fit"> <!-- bilde fra google -->
    <p>Overwatch 2 har 8 "ranks" eller rang på norsk: bronze, silver, gold, platinum, diamond, masters, grandmasters og champion. Hver rank har 5 divisjoner og du må vinne kamper for å stige i rank.</p>
    <p>Å klatre til neste divisjon i din rank tar rundt 5 runder. Man taper omtrent like mye poeng hver gang man taper som man mottar hver gang man vinner. Det betyr at du må vinne flere kamper enn du taper for å komme til neste rank</p>
    
